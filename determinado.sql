
DELIMITER //
CREATE FUNCTION calcular_cuadrado(n INT)
RETURNS INT
DETERMINISTIC 
BEGIN
RETURN n*n;
END //


DELIMITER //

CREATE FUNCTION suma_simple(a INT, b INT) 
RETURNS INT
DETERMINISTIC
BEGIN
    RETURN a + b;
END //

DELIMITER ;



DELIMITER //

CREATE FUNCTION horaActual() 
RETURNS TIME
DETERMINISTIC
BEGIN
    RETURN NOW();
END //

DELIMITER ;